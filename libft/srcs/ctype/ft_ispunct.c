#include "ft_ctype.h"

int	ft_ispunct(int c)
{
	return ((c >= 041 && c <= 057) || (c >= 072 && c <= 0100)
		|| (c >= 0133 && c <= 0140) || (c >= 0173 && c <= 0176));
}
